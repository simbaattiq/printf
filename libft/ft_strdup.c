#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dup;
	size_t	i;
	size_t	len;

	len = ft_strlen(str);
	i = 0;
	dup = (char *)malloc (len + 1);
	if (!dup)
		return (NULL);
	while (str[i] != '\0')
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
